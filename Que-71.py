#71) File Functions in Python & Keywords for Creating and Writing Files:
#•	File Functions include methods like open(), read(), write(), and close().
#•	Keywords for File Modes:
#o	'r': Read
#o	'w': Write (overwrites existing content)
#o	'a': Append (adds content to end)
#o	'r+': Read and Write
#o	'w+': Write and Read (overwrites file)
#o	'a+': Append and Read
