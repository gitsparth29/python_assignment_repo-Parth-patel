#25) What is a List? How Will You Reverse a List?
#•	A list in Python is a collection of items, which can contain multiple data types (e.g., integers, strings). Lists are mutable, meaning you can modify their contents after they are created.
#•	To reverse a list, you can use:

my_list.reverse()  # In-place reversal
# or
reversed_list = my_list[::-1]  # Slicing to create a reversed copy

